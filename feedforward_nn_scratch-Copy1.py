    "def make_classification_data(n_samples=200, seed=42):\n",
    "    \"\"\"\n",
    "    Generates a 2D synthetic classification dataset with two interleaved spirals.\n",
    "    This version is *less challenging* than the original by reducing the noise\n",
    "    and lowering the number of rotations in the spiral.\n",
    "    \"\"\"\n",
    "    np.random.seed(seed)\n",
    "\n",
    "    n_class0 = n_samples // 2\n",
    "    n_class1 = n_samples - n_class0\n",
    "\n",
    "    X = []\n",
    "    y = []\n",
    "\n",
    "    # Generate points for class 0 spiral\n",
    "    for i in range(n_class0):\n",
    "        r = 5.0 * (i / n_class0)               # radius grows with i\n",
    "        theta = 1.0 * (i / n_class0) * 2 * np.pi  # fewer turns (changed from 1.75)\n",
    "        # Reduced noise multiplier from 0.2 to 0.05\n",
    "        x_coord = r * np.sin(theta) + np.random.randn() * 0.05\n",
    "        y_coord = r * np.cos(theta) + np.random.randn() * 0.05\n",
    "\n",
    "        X.append([x_coord, y_coord])\n",
    "        y.append(0)\n",
    "\n",
    "    # Generate points for class 1 spiral\n",
    "    for i in range(n_class1):\n",
