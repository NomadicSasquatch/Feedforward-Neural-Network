    "class OptimiserBase:\n",
    "    \"\"\"Base class for all optimisers.\"\"\"\n",
    "    def step(self, params: List[np.ndarray], grads: List[np.ndarray]):\n",
    "        raise NotImplementedError(\"step method must be implemented by subclass optimisers.\")\n",
    "\n",
    "class SGD(OptimiserBase):\n",
    "    \"\"\"\n",
    "    Stochastic Gradient Descent with no momentum.\n",
    "\n",
    "    Attributes:\n",
    "        lr (float): Learning rate.\n",
    "    \"\"\"\n",
    "    def __init__(self, lr: float = 0.01):\n",
    "        self.lr = lr\n",
    "\n",
    "    def step(self, params: List[np.ndarray], grads: List[np.ndarray]):\n",
    "        for p, g in zip(params, grads):\n",
    "            p -= self.lr * g\n",
    "\n",
    "class SGDWithMomentum(OptimiserBase):\n",
    "    \"\"\"\n",
    "    Stochastic Gradient Descent with Momentum.\n",
    "    v = momentum * v - lr * grad\n",
    "    param += v\n",
    "    \"\"\"\n",
    "    def __init__(self, lr: float = 0.01, momentum: float = 0.9):\n",
    "        self.lr = lr\n",
    "        self.momentum = momentum\n",
    "        self.v = None  # Initialize after seeing param shapes\n",
    "\n",
    "    def step(self, params: List[np.ndarray], grads: List[np.ndarray]):\n",
    "        if self.v is None:\n",
    "            self.v = [np.zeros_like(p) for p in params]\n",
    "\n",
    "        for i, (p, g) in enumerate(zip(params, grads)):\n",
    "            self.v[i] = self.momentum * self.v[i] - self.lr * g\n",
    "            p += self.v[i]\n",
    "\n",
    "class Adam(OptimiserBase):\n",
    "    \"\"\"\n",
    "    Adam optimiser from scratch.\n",
    "\n",
    "    Attributes:\n",
    "        lr (float): Learning rate.\n",
    "        beta1 (float): Exponential decay rate for the first moment.\n",
    "        beta2 (float): Exponential decay rate for the second moment.\n",
    "        eps (float): Small constant to avoid division by zero.\n",
    "    \"\"\"\n",
    "    def __init__(self, lr: float = 0.001, beta1: float = 0.9, beta2: float = 0.999, eps: float = 1e-8):\n",
    "        self.lr = lr\n",
    "        self.beta1 = beta1\n",
    "        self.beta2 = beta2\n",
    "        self.eps = eps\n",
    "        self.t = 0\n",
    "        self.m = None\n",
    "        self.v = None\n",
    "\n",
    "    def step(self, params: List[np.ndarray], grads: List[np.ndarray]):\n",
    "        if self.m is None:\n",
    "            self.m = [np.zeros_like(p) for p in params]\n",
    "            self.v = [np.zeros_like(p) for p in params]\n",
    "\n",
    "        self.t += 1\n",
    "        for i, (p, g) in enumerate(zip(params, grads)):\n",
    "            self.m[i] = self.beta1 * self.m[i] + (1 - self.beta1) * g\n",
    "            self.v[i] = self.beta2 * self.v[i] + (1 - self.beta2) * (g * g)\n",
    "\n",
    "            m_hat = self.m[i] / (1 - self.beta1 ** self.t)\n",
    "            v_hat = self.v[i] / (1 - self.beta2 ** self.t)\n",
    "\n",
    "            p -= self.lr * m_hat / (np.sqrt(v_hat) + self.eps)"
