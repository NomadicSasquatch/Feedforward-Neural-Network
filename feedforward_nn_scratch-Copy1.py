    "def sigmoid(x: np.ndarray) -> np.ndarray:\n",
    "    \"\"\"Sigmoid activation function.\"\"\"\n",
    "    return 1.0 / (1.0 + np.exp(-x))  # No change in logic\n",
    "\n",
    "def sigmoid_deriv(x: np.ndarray) -> np.ndarray:\n",
    "    \"\"\"\n",
    "    Computes the derivative of the sigmoid function.\n",
    "\n",
    "    d/dx(sigmoid(x)) = sigmoid(x) * (1 - sigmoid(x))\n",
    "    \"\"\"\n",
    "    s = sigmoid(x)\n",
    "    return s * (1.0 - s)\n",
    "\n",
    "def relu(x: np.ndarray) -> np.ndarray:\n",
    "    \"\"\"ReLU activation function.\"\"\"\n",
    "    return np.maximum(0, x)\n",
    "\n",
    "def relu_deriv(x: np.ndarray) -> np.ndarray:\n",
    "    \"\"\"Derivative of the ReLU activation function.\"\"\"\n",
    "    grad = np.zeros_like(x)\n",
    "    grad[x > 0] = 1\n",
    "    return grad\n",
    "\n",
